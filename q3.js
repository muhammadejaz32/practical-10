let vehicle = "truck"
    switch(vehicle) {
        case "car":
            console.log( "Car");
            break;
        case "truck":
            console.log( "Truck");
             break;
        case "motorcycle":
            console.log( "Motorcycle"); 
            break;
        case "bicycle":
            console.log( "Bicycle"); 
            break;
        case "bus":
            console.log( "Bus");
             break;
        case "van":
            console.log( "Van"); 
            break;
        default:
            console.log( "Unknown vehicle type."); 
    }